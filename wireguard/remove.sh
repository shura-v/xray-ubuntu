  echo "‚ùå –ö–ª–∏–µ–Ω—Ç '$CLIENT_NAME' –Ω–µ –Ω–∞–π–¥–µ–Ω –≤ –∫–æ–Ω—Ñ–∏–≥–µ!"
  exit 1
